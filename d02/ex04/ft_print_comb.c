void	ft_print_comb(void)
{
	int a;
	int b;
	int c;

	a = '0' - 1;
	while (++a <= '9')
	{
		b = a;
		while (++b <= '9')
		{
			c = b;
			while (++c <= '9')
			{
				ft_putchar(a);
				ft_putchar(b);
				ft_putchar(c);
				if (a + b + c < 168)
				{
					ft_putchar(44);
					ft_putchar(32);
				}
			}
		}
	}
}
