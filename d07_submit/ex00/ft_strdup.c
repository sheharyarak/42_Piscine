#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*str;

	i = 0;
	str = (char*)malloc(sizeof(*src) + 1);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	return (str);
}
